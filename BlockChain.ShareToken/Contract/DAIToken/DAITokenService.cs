        public static Task<TransactionReceipt> DeployContractAndWaitForReceiptAsync(Nethereum.Web3.Web3 web3, DAITokenDeployment dAITokenDeployment, CancellationTokenSource cancellationTokenSource = null)
        {
            return web3.Eth.GetContractDeploymentHandler<DAITokenDeployment>().SendRequestAndWaitForReceiptAsync(dAITokenDeployment, cancellationTokenSource);
        }

        public static Task<string> DeployContractAsync(Nethereum.Web3.Web3 web3, DAITokenDeployment dAITokenDeployment)
        {
            return web3.Eth.GetContractDeploymentHandler<DAITokenDeployment>().SendRequestAsync(dAITokenDeployment);
        }

        public static async Task<DAITokenService> DeployContractAndGetServiceAsync(Nethereum.Web3.Web3 web3, DAITokenDeployment dAITokenDeployment, CancellationTokenSource cancellationTokenSource = null)
        {
            var receipt = await DeployContractAndWaitForReceiptAsync(web3, dAITokenDeployment, cancellationTokenSource);
            return new DAITokenService(web3, receipt.ContractAddress);
        }

